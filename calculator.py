
def addition(a, b):

    #Performs addition of two numbers.

    result = a + b
    return result

def subtraction(a, b):

    #Performs subtraction of two numbers.


    result = a - b
    return result

def multiplication(a, b):

    #Performs multiplication of two numbers.


    result = a * b
    return result

def division(a, b):

    #Performs division of two numbers.

    if b == 0:
        return "Error: Cannot divide by zero."
    result = a / b
    return result
